# suma y resta
suma = 5 + 3
resta = 5 - 3

#multiplicacion y division
multiplicacion = 5 * 3
division = 5 / 3 #devuelve un float 

#potencia y division entera
potencia = 5 ** 3 #5 elevado a la 3
division_entera = 5 // 3 #devuelve un entero

#resto de la division
resto = 5 % 3 #devuelve el resto de la division

print("suma: ", suma)
print("resta: ", resta)
print("multiplicacion: ", multiplicacion)
print("division: ", division)
print("potencia: ", potencia)
print("division entera: ", division_entera)
print("resto: ", resto)

